    getAllUnits: function() {
        return this.units;
    },
    
    // Get unit by ID
    getUnitById: function(id) {
        return this.units.find(unit => unit.id === id);
    },
    
    // Search units by keyword
    searchUnits: function(keyword) {
        const searchTerm = keyword.toLowerCase();
        return this.units.filter(unit => 
            unit.title.toLowerCase().includes(searchTerm) ||
            unit.location.toLowerCase().includes(searchTerm) ||
            unit.category.toLowerCase().includes(searchTerm) ||
            (unit.subheadings && unit.subheadings.some(sh => sh.toLowerCase().includes(searchTerm))) ||
            (unit.paragraphs && unit.paragraphs.some(p => p.toLowerCase().includes(searchTerm))) ||
            (unit.bulletPoints && unit.bulletPoints.some(bp => bp.toLowerCase().includes(searchTerm)))
        );
    },
    
    // Filter units by category
    filterByCategory: function(category) {
        if (category === 'all') return this.units;
        const categoryMap = {
            'religious': 'धार्मिक',
            'historical': 'ऐतिहासिक',
            'tourist': 'पर्यटकीय'
        };
        const nepaliCategory = categoryMap[category] || category;
        return this.units.filter(unit => unit.category === nepaliCategory);
    },
    
    // Get units by district
    getUnitsByDistrict: function(district) {
        return this.units.filter(unit => unit.location.includes(district));
    },
    
    // Get featured units (first 10)
    getFeaturedUnits: function() {
        return this.units.slice(0, 10);
    },
    
    // Get units with altitude above certain level
    getHighAltitudeUnits: function(minAltitude) {
        return this.units.filter(unit => {
            const alt = unit.altitude ? parseInt(unit.altitude.replace(/[^\d]/g, '')) : 0;
            return alt >= minAltitude;
        });
    },
    
    // Get recently updated units (2024-2025)
    getRecentlyUpdatedUnits: function() {
        return this.units.filter(unit => 
            unit.factCheck && unit.factCheck.year >= 2024
        );
    }
